use crate::error::SnipResult;

/// Supported shell types for integration generation.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ShellType {
    Bash,
    Zsh,
    Fish,
}

impl std::fmt::Display for ShellType {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            ShellType::Bash => write!(f, "bash"),
            ShellType::Zsh => write!(f, "zsh"),
            ShellType::Fish => write!(f, "fish"),
        }
    }
}

/// Generate and print shell integration code for the specified shell.
pub fn run(shell: ShellType) -> SnipResult<()> {
    let code = match shell {
        ShellType::Bash => generate_bash(),
        ShellType::Zsh => generate_zsh(),
        ShellType::Fish => generate_fish(),
    };
    print!("{code}");
    Ok(())
}

/// Multiline transport: all shell adapters write the selection to a temp file
/// via `--output-file`, then read it back. This avoids shell-specific quoting
/// issues with `$(...)` stripping trailing newlines and NUL bytes.
fn generate_bash() -> String {
    r#"# Bash integration for snp — Generated by `snp shell init bash`
# Adds snp_select_raw and snp_select_expanded functions.
# No keybindings are installed by default. Bind manually if desired.
# Inspect this output before sourcing.

__snp_select() {
  if ! command -v snp &>/dev/null; then
    echo "snp: 'snp' not found in PATH" >&2
    return 1
  fi

  local mode="$1"
  local old_line="$READLINE_LINE"
  local old_point="$READLINE_POINT"

  local tmp_file
  tmp_file="$(mktemp "${TMPDIR:-/tmp}/snp-select.XXXXXX")"

  local args=(--output-file "$tmp_file" --"$mode")
  if [[ -n "$READLINE_LINE" ]]; then
    args+=(--query "$READLINE_LINE")
  fi

  snp select "${args[@]}"
  local rc=$?

  if [[ $rc -eq 4 ]] || [[ ! -s "$tmp_file" ]]; then
    rm -f "$tmp_file"
    READLINE_LINE="$old_line"
    READLINE_POINT="$old_point"
    return 0
  fi

  if [[ $rc -ne 0 ]]; then
    rm -f "$tmp_file"
    READLINE_LINE="$old_line"
    READLINE_POINT="$old_point"
    return 1
  fi

  local selected
  IFS= read -r -d '' selected < "$tmp_file"
  rm -f "$tmp_file"

  READLINE_LINE="$selected"
  READLINE_POINT="${#selected}"
}

snp_select_raw() {
  __snp_select raw
}

snp_select_expanded() {
  __snp_select expanded
}

# To bind manually, add to your .bashrc:
#   bind -x '"\C-o": snp_select_raw'
#   bind -x '"\C-o\C-e": snp_select_expanded'
"#
    .to_string()
}

fn generate_zsh() -> String {
    r#"# Zsh integration for snp — Generated by `snp shell init zsh`
# Adds snp_select_raw and snp_select_expanded ZLE widgets.
# No keybindings are installed by default. Bind manually with:
#   bindkey '^O' snp_select_raw
# Inspect this output before sourcing.

__snp_select() {
  if ! command -v snp &>/dev/null; then
    echo "snp: 'snp' not found in PATH" >&2
    return 1
  fi

  local mode="$1"
  local old_buffer="$BUFFER"
  local old_cursor="$CURSOR"

  local tmp_file
  tmp_file="$(mktemp "${TMPDIR:-/tmp}/snp-select.XXXXXX")"

  local -a args=(--output-file "$tmp_file" --"$mode")
  if [[ -n "$BUFFER" ]]; then
    args+=(--query "$BUFFER")
  fi

  snp select "${args[@]}"
  local rc=$?

  if [[ $rc -eq 4 ]] || [[ ! -s "$tmp_file" ]]; then
    rm -f "$tmp_file"
    BUFFER="$old_buffer"
    CURSOR="$old_cursor"
    zle redisplay
    return
  fi

  if [[ $rc -ne 0 ]]; then
    rm -f "$tmp_file"
    BUFFER="$old_buffer"
    CURSOR="$old_cursor"
    zle redisplay
    return
  fi

  local selected
  IFS= read -r -d '' selected < "$tmp_file"
  rm -f "$tmp_file"

  BUFFER="$selected"
  CURSOR="${#BUFFER}"
  zle redisplay
}

zle -N snp_select_raw () { __snp_select raw }
zle -N snp_select_expanded () { __snp_select expanded }

# To bind, add to your .zshrc:
#   bindkey '^O' snp_select_raw
#   bindkey '^O^E' snp_select_expanded
"#
    .to_string()
}

fn generate_fish() -> String {
    r#"# Fish integration for snp — Generated by `snp shell init fish`
# Adds snp_select_raw and snp_select_expanded functions.
# No keybindings are installed by default. Bind manually with:
#   bind \co snp_select_raw
# Inspect this output before sourcing.

function __snp_select --argument-names mode
  if not command -q snp
    echo "snp: 'snp' not found in PATH" >&2
    return 1
  end

  set -l old_cmdline (commandline)
  set -l old_cursor (commandline -C)

  set -l tmp_file (mktemp "/tmp/snp-select.XXXXXX")

  set -l snp_args --output-file "$tmp_file" --"$mode"
  if test -n "$old_cmdline"
    set snp_args $snp_args --query "$old_cmdline"
  end

  snp select $snp_args
  set -l rc $status

  if test $rc -eq 4 -o ! -s "$tmp_file"
    rm -f "$tmp_file"
    commandline -C "$old_cursor"
    commandline -r "$old_cmdline"
    return
  end

  if test $rc -ne 0
    rm -f "$tmp_file"
    commandline -C "$old_cursor"
    commandline -r "$old_cmdline"
    return 1
  end

  set -l selected (cat "$tmp_file" | string split0)
  rm -f "$tmp_file"

  commandline -r "$selected"
  commandline -C (string length -- "$selected")
end

function snp_select_raw
  __snp_select raw
end

function snp_select_expanded
  __snp_select expanded
end

# To bind, add to your config.fish:
#   bind \co snp_select_raw
#   bind \co\e snp_select_expanded
"#
    .to_string()
}

