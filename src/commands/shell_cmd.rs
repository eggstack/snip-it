    // H5: Non-execution sentinel — verify widget doesn't execute

    #[test]
    fn test_bash_does_not_execute_on_source() {
        if !shell_exists("bash") {
            eprintln!("skipping: bash not found");
            return;
        }
        let sentinel = tempfile::NamedTempFile::new().unwrap();
        let sentinel_path = sentinel.path().to_str().unwrap().to_string();
        drop(sentinel);

        let code = generate_bash();
        let script = format!(
            "{}\nsnp_select_raw() {{ echo '{}' > '{}'; }}",
            code, "SENTINEL_CREATED", sentinel_path
        );

        let tmp = tempfile::NamedTempFile::new().unwrap();
        std::fs::write(tmp.path(), &script).unwrap();

        // Source the file — functions are defined but not invoked
        let output = Command::new("bash")
            .args(["-c", &format!(". {}", tmp.path().to_str().unwrap())])
            .output()
            .expect("failed to run bash");

        assert!(
            output.status.success(),
            "bash source failed: {}",
            String::from_utf8_lossy(&output.stderr)
        );

        // Verify sentinel was NOT created
        assert!(
            !std::path::Path::new(&sentinel_path).exists(),
            "sentinel file was created during source — non-execution violated"
        );
    }

